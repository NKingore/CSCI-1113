#include <iostream>

int main(){

    //integer division examples
    int result = 355/113;
    int result2 = 355.0/113;
    double result3 = 355/113;
    double result4 = 355.0/113;

    std::cout << "1. " << result << std::endl;
    std::cout << "2. " << result2 << std::endl;
    std::cout << "3. " << result3 << std::endl;
    std::cout << "4. " << result4 << std::endl;

    //modulo examples. modulo evaluates the remainder.
    int result5 = 21 / 8;
    int result6 = 21 % 8;

    std::cout << "5. " << result5 << std::endl;
    std::cout << "6. " << result6 << std::endl;

    
}